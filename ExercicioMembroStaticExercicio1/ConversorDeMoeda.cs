

namespace ExercicioMembroStaticExercicio1 {
    internal class ConversorDeMoeda {

        public static double Iof = 6.0;

        public static double Real(double moedaD, double QmoedaD) {
            double total = moedaD * QmoedaD;
            return total = total + total * Iof / 100;
        }



    }
}
